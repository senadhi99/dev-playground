Function Generate-Captions {
    param([PSCustomObject]$cat)

    $suffixList = @("", "!", "...", " :)", " today.", " mode.", " feeling.", " vibes.")
    $results = @()
    # Ensure minimum 50 captions by duplicating the template array with slight mutations (suffixes)
    $count = 0
    while($results.Count -lt 50) {
        foreach($t in $cat.templates) {
            if ($results.Count -ge 50) { break }
            if ($count -eq 0) {
                # Add base templates
                $results += $t
            } else {
                # Add random text mutation
                $s = Get-Random -InputObject @("", "!", "...", " :)", " today.", " mode.", " feeling.", " vibes.")
                if ($t.EndsWith(".")) {
                    $results += ($t.Substring(0, $t.Length - 1) + $s)
                } else {
                    $results += "$t$s"
                }
            }
        }
        $count++
    }
    return $results
}

