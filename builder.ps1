    If (!(Test-Path $folder)) { New-Item -ItemType Directory -Path $folder | Out-Null }

    $lHtml = $headerTemplate.Replace("{{TITLE}}", $page[1]).Replace("{{DESC}}", $page[1]).Replace("{{ROOT_PATH}}", "../")
    $lHtml += "
        <div class=`"page-container`">
            <div class=`"seo-intro`">
                <h1>$($page[1])</h1>
                <p>$($page[2])</p>
            </div>
        </div>
"
    $lHtml += $footerTemplate.Replace("{{ROOT_PATH}}", "../")

    $htmlPath = Join-Path -Path $folder -ChildPath "index.html"
    [IO.File]::WriteAllText($htmlPath, $lHtml)
}

Write-Host "Website Built Successfully!"
